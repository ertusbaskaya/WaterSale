public class Costumers {
    private String costumersID;
    private String costumersName;
    private String costumersSurname;
    private String costumersPhone;
    private String costumersAdress;

    public String getCostumersID() {
        return costumersID;
    }

    public void setCostumersID(String costumersID) {
        this.costumersID = costumersID;
    }

    public String getCostumersName() {
        return costumersName;
    }

    public void setCostumersName(String costumersName) {
        this.costumersName = costumersName;
    }

    public String getCostumersSurname() {
        return costumersSurname;
    }

    public void setCostumersSurname(String costumersSurname) {
        this.costumersSurname = costumersSurname;
    }

    public String getCostumersPhone() {
        return costumersPhone;
    }

    public void setCostumersPhone(String costumersPhone) {
        this.costumersPhone = costumersPhone;
    }

    public String getCostumersAdress() {
        return costumersAdress;
    }

    public void setCostumersAdress(String costumersAdress) {
        this.costumersAdress = costumersAdress;
    }
    
}
