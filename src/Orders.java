public class Orders {
    private String ordersID;
    private String ordersName;
    private String ordersSurname;
    private String ordersPhone;
    private String ordersAdress;
    private String ordersStatus;
    private String ordersCost;

    public String getOrdersID() {
        return ordersID;
    }

    public void setOrdersID(String ordersID) {
        this.ordersID = ordersID;
    }

    public String getOrdersName() {
        return ordersName;
    }

    public void setOrdersName(String ordersName) {
        this.ordersName = ordersName;
    }

    public String getOrdersSurname() {
        return ordersSurname;
    }

    public void setOrdersSurname(String ordersSurname) {
        this.ordersSurname = ordersSurname;
    }

    public String getOrdersPhone() {
        return ordersPhone;
    }

    public void setOrdersPhone(String ordersPhone) {
        this.ordersPhone = ordersPhone;
    }

    public String getOrdersAdress() {
        return ordersAdress;
    }

    public void setOrdersAdress(String ordersAdress) {
        this.ordersAdress = ordersAdress;
    }

    public String getOrdersStatus() {
        return ordersStatus;
    }

    public void setOrdersStatus(String ordersStatus) {
        this.ordersStatus = ordersStatus;
    }

    public String getOrdersCost() {
        return ordersCost;
    }

    public void setOrdersCost(String ordersCost) {
        this.ordersCost = ordersCost;
    }
    
}
